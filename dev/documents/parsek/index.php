<h1>Documentation for Parsek client</h1>

<p>Parsek is a free and open source game client for playing 4X (explore, expand, exploit and exterminate) strategy games created by Thousand Parsec project. Parsek is designed for KDE 4 desktop and should run at least on Linux, Mac OS X and Windows.</p>

<p>Development information for Parsek is available on <strong><a href="http://www.thousandparsec.net/wiki/Parsek">Thousand Parsec Wiki</a></strong>.</p>

